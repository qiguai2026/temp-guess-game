// 获取所有词汇（扁平化数组）
function getAllWords() {
  return Object.values(wordDatabase).flat();
}

// 获取词汇分类
function getWordCategory(word) {
  for (const [category, words] of Object.entries(wordDatabase)) {
    if (words.includes(word)) return category;
  }
  return null;
}

// 随机获取一个词汇
function getRandomWord() {
  const allWords = getAllWords();
  return allWords[Math.floor(Math.random() * allWords.length)];
}

// 获取同一类别的词汇
function getWordsInSameCategory(word) {
  const category = getWordCategory(word);
  if (!category) return [];
  return wordDatabase[category].filter(w => w !== word);
}

